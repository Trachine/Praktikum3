# input variabel
w, x, y, z = 10, 15, 20, 25 

# menampilkan varible
print(w, x, y, z)

# menampilkan varible dengan "," sebagai pemisah antar variable 
print(w, x, y, z, sep=',')

# menampilkan varible dengan tidak menggunakan pemisah antar variable 
print(w, x, y, z, sep='')

# menampilkan varible dengan ":" sebagai pemisah antar variable 
print(w, x, y, z, sep=':')

# menampilkan varible dengan "-" sebagai pemisah antar variable 
print(w, x, y, z, sep='-----')
